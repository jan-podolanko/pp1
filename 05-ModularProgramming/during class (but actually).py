import math
x4 = 3.7
y4 = 4
#a
sqrtx4 = math.sqrt(x4)
#b
powx4y4 = math.pow(x4,y4)
#c
rootx4 = math.pow(x4,(1/y4))
#d
circley4 = math.pi * y4 ** 2 
#e
facty4 = math.factorial(y4)
#f
smallerorequalx4 = math.floor(x4)

print(f'Pierwiastek kwadratowy z {x4} wynosi {sqrtx4}.')
print(f'{x4} do potęgi {y4} wynosi {powx4y4}.')
print(f'Pierwiastek z {x4} stopnia {y4} wynosi {rootx4}.')
print(f'Pole koła o promieniu {y4} wynosi {circley4}')
print(f'Silnia {y4} wynosi {facty4}')
print(f'Największą liczbą całkowitą mniejszą lub równą {x4} jest {smallerorequalx4}')